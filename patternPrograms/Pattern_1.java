package patternPrograms;

public class Pattern_1 {
	
	public static void main(String[] args)
	{
		
		
		for(int i=0;i<=4;i++) //  column switch
		{
			for(int j=0;j<=4;j++)
			{
				System.out.print("*"); //row wise

				
			}
			System.out.println();
			
		}
	}

}

